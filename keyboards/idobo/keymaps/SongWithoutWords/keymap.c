static bool g_keyDownEscape = false;
static bool g_keyDownBSpace = false;
static bool g_keyDownLCtrl  = false;
static bool g_keyDownRCtrl  = false;
static bool g_keyDownQ  = false;
static bool g_keyDownC  = false;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {

  const bool pressed = record->event.pressed;

  switch (keycode) {
  case KC_ESC:
    g_keyDownEscape = pressed;
    break;
  case KC_BSPC:
    g_keyDownBSpace = pressed;
    break;
  case KC_LCTRL:
    g_keyDownLCtrl = pressed;
    break;
  case KC_RCTRL:
    g_keyDownRCtrl = pressed;
    break;
  case KC_Q:
    g_keyDownQ = pressed;
    break;
  case KC_C:
    g_keyDownC = pressed;
    break;
  }

  if (pressed) {
    switch (keycode) {
    case KC_ESC:
    case KC_BSPC:
    case KC_LCTRL:
    case KC_RCTRL:
    case KC_Q:
    case KC_C:
      if (g_keyDownEscape && g_keyDownBSpace && g_keyDownLCtrl && g_keyDownRCtrl) {
        reset_keyboard();
        return false;
      }
      if (g_keyDownEscape && g_keyDownBSpace && g_keyDownLCtrl && g_keyDownQ) {
        set_single_persistent_default_layer(QWERTY);
        return false;
      }
      if (g_keyDownEscape && g_keyDownBSpace && g_keyDownLCtrl && g_keyDownC) {
        set_single_persistent_default_layer(COLEMAK);
        return false;
      }
    }
  }

  return true;
}
